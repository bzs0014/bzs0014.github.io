const translatableNodes = document.querySelectorAll("[data-i18n]");
const langButtons = document.querySelectorAll(".lang-button");
const yearNode = document.querySelector("#current-year");
const copyEmailButton = document.querySelector("[data-copy-email]");
const sectionLinks = document.querySelectorAll("[data-section-link]");
const observedSections = [...sectionLinks]
  .map((link) => document.getElementById(link.dataset.sectionLink))
  .filter(Boolean);

let currentLanguage = localStorage.getItem("preferred-language") || "en";
let copyResetTimer;

function setLanguage(language) {
  const dictionary = translations[language] ?? translations.en;
  currentLanguage = translations[language] ? language : "en";
  document.documentElement.lang = currentLanguage === "zh" ? "zh-CN" : "en";

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === currentLanguage;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("preferred-language", currentLanguage);
}

function setActiveSection(sectionId) {
  sectionLinks.forEach((link) => {
    link.classList.toggle("is-active", link.dataset.sectionLink === sectionId);
  });
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

if (copyEmailButton) {
  copyEmailButton.addEventListener("click", async () => {
    const email = copyEmailButton.dataset.copyEmail;
    try {
      await navigator.clipboard.writeText(email);
      copyEmailButton.textContent = translations[currentLanguage]["actions.copied"];
      clearTimeout(copyResetTimer);
      copyResetTimer = setTimeout(() => {
        copyEmailButton.textContent = translations[currentLanguage]["actions.copyEmail"];
      }, 1800);
    } catch {
      copyEmailButton.textContent = email;
    }
  });
}

if ("IntersectionObserver" in window && observedSections.length) {
  const observer = new IntersectionObserver(
    (entries) => {
      const visible = entries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (visible) {
        setActiveSection(visible.target.id);
      }
    },
    { rootMargin: "-35% 0px -55% 0px", threshold: [0.1, 0.25, 0.5] },
  );

  observedSections.forEach((section) => observer.observe(section));
}

if (yearNode) {
  yearNode.textContent = new Date().getFullYear();
}

setLanguage(currentLanguage);
